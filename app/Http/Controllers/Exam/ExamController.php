    /** return list of current exams only for a particular institution
     * @param int $id
     */
    public function currentExam($id){
      $current_exams = Exam::join('exam_schedule','exam.id','=', 'exam_schedule.exam_id')
                      ->where('exam_schedule.active','=',1)
                      ->where('exam.institution_id', $id)
                      ->get(['exam_schedule.id as id','exam.name']);
      
      return response()->json(compact('current_exams'));
    }
